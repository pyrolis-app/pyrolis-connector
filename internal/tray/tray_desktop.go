// Tray holds the systray state.
type Tray struct {
	port    int
	version string
	quitCh  chan struct{}
}

// New creates a new Tray.
func New(port int, version string) *Tray {
	return &Tray{
		port:    port,
		version: version,
		quitCh:  make(chan struct{}),
	}
}

// Run starts the system tray. This blocks until quit is selected.
func (t *Tray) Run() {
	systray.Run(t.onReady, t.onExit)
}

// Quit signals the tray to exit.
func (t *Tray) Quit() {
	systray.Quit()
}

// QuitCh returns a channel that is closed when the user clicks Quit.
func (t *Tray) QuitCh() <-chan struct{} {
	return t.quitCh
}

// SetStatus updates the status tooltip text.
func (t *Tray) SetStatus(status string) {
	systray.SetTooltip(fmt.Sprintf("Pyrolis Connector v%s - %s", t.version, status))
}

func (t *Tray) onReady() {
	systray.SetIcon(iconData)
	systray.SetTitle("Pyrolis")
	systray.SetTooltip(fmt.Sprintf("Pyrolis Connector v%s", t.version))

	mDashboard := systray.AddMenuItem("Open Dashboard", "Open the web dashboard")
	systray.AddSeparator()
	mCheckUpdate := systray.AddMenuItem("Check for Updates", "Check GitHub for a new version")
	systray.AddSeparator()
	mQuit := systray.AddMenuItem("Quit", "Stop the connector")

	go func() {
		for {
			select {
			case <-mDashboard.ClickedCh:
				openURL(fmt.Sprintf("http://localhost:%d", t.port))
			case <-mCheckUpdate.ClickedCh:
				// Trigger update check via web API
				openURL(fmt.Sprintf("http://localhost:%d/update/check", t.port))
			case <-mQuit.ClickedCh:
				close(t.quitCh)
				systray.Quit()
				return
			}
		}
	}()
}

func (t *Tray) onExit() {
	// Cleanup if needed
}

func openURL(url string) {
	var cmd *exec.Cmd
	switch runtime.GOOS {
	case "windows":
		cmd = exec.Command("cmd", "/c", "start", url)
	case "darwin":
		cmd = exec.Command("open", url)
	default:
		cmd = exec.Command("xdg-open", url)
	}
	_ = cmd.Start()
}
